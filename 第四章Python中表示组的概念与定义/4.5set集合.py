#coding=utf-8

#集合的定义
print(type({1,3, 4}))#set

#长度
print(len({1,3, 4}))#3

#是否包含某个元素
print(3 in {1,3, 4})#True

#求两个集合的差集
print({1, 2, 3, 4, 5, 6} - {2,3});#set([1, 4, 5, 6])

#求两个集合的交集
print({1, 2, 3, 4, 5, 6} & {2,3});#set([2, 3])

#求两个集合的合集
print({1, 2, 3, 4, 5, 6} | {2,3,10});#set([1, 2, 3, 4, 5, 6, 10])

#定义一个空的集合
print(set());#set([])